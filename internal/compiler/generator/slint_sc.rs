//! Code generator for the Slint SC (safety-critical) runtime.
//!
//! Slint SC is a drastically reduced subset of Slint: no layouts, no
//! callbacks, no models, no text, no animations, and only three visual
//! elements (`Window`, `Rectangle`, `Image`).  The generator walks the
//! post-pass `object_tree` directly — LLR would be overkill — and emits a
//! small, auditable Rust module that depends only on the `slint_sc` runtime
//! crate.  Anything outside the supported subset causes a compile error with
//! a clear diagnostic.
//!
//! The generated code shape for
//!
//! ```slint,ignore
//! export component Hello inherits Window {
//!     width: 100px; height: 50px;
//!     Rectangle { x: 10px; y: 10px; width: 20px; height: 20px; background: red; }
//! }
//! ```
//!
//! is roughly
//!
//! ```ignore
//! pub struct Hello { /* user-declared properties as `Property<T>` */ }
//! impl Hello {
//!     pub fn new() -> Self { ... }
//!     pub fn render<B: slint_sc::TargetPixelBuffer>(&self, buffer: &mut B) {
//!         slint_sc::private_unstable_api::fill_rectangle(buffer, 10, 10, 20, 20, Color::...);
//!     }
//! }
//! ```

